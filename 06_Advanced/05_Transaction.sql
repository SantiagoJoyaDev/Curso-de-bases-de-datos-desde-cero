------------ TRANSACTION ------------

/*Una transacción es un conjunto de operaciones SQL que se ejecutan de forma atómica, es decir, todas ocurren o ninguna 
(si alguna falla, se deshacen las ya realizadas). Las transacciones garantizan la integridad de los datos en operaciones 
que involucran múltiples cambios.
Comandos básicos:
● START TRANSACTION; (o BEGIN;) inicia una nueva transacción.
● COMMIT; confirma la transacción, haciendo permanentes todos los cambios realizados desde el inicio de la transacción.
● ROLLBACK; revierte todos los cambios hechos en la transacción, dejando*/